package database;

public class User {
	private String name;
	private int topScore;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTopScore() {
		return topScore;
	}
	public void setTopScore(int topScore) {
		this.topScore = topScore;
	}
	

}
